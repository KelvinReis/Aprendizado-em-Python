#Antecessor e sucessor
#Algoritmo que mostra o sucessor e antecessor de qualquer número
#Definindo a função para o antecessor
def antecessor(num):
    return num - 1
assert 5 == antecessor(6)
#Definindo a função para o sucessor
def sucessor(num):
    return num+1
assert 10 == sucessor(9)
