#Descobrir os dias de 'n' meses
#Considerar que todos os meses possuem 30 dias, calcular o total de dias de n meses
#Criando a função diasMes
def diasMes(dias):
    return dias * 30
#Testando o resultado
assert diasMes(5) == 150
