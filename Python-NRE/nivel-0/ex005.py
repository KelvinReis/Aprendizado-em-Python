#Média simples de 3 números
#Calcular a média simples de 3 valores qualquer
#Criando a função
def media(valor1, valor2, valor3):
    return (valor1 + valor2 + valor3) / 3
#Testando o resultado
assert 10 == media(10, 10, 10)
